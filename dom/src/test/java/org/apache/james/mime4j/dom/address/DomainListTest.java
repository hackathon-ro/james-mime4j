package org.apache.james.mime4j.dom.address;

import org.junit.Test;

import java.util.ArrayList;
import java.util.List;

import static org.hamcrest.core.Is.is;
import static org.junit.Assert.assertThat;

/**
 * DomainList is not immutable!!
 */
public class DomainListTest {

    @Test
    public void testFalseImmutability() throws Exception {
        final List<String> list = new ArrayList<String>();
        final DomainList domainList = new DomainList(list, true);

        assertThat(domainList.size(), is(0));
        list.add("fake");
        list.add("immutability");
        assertThat(domainList.size(), is(2));
    }
}
